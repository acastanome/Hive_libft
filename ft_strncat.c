#include "libft.h"

char	*ft_strncat(char *s1, const char *s2, size_t n)
{
	size_t	s1len;
	size_t	s2len;

	s1len = ft_strlen(s1);
	s2len = ft_strlen(s2);
	if (s2len <= n)
		n = s2len;
	ft_memcpy(s1 + s1len, s2, n);
	s1[s1len + n] = '\0';
	return (s1);
}
